class UnaryOperators
{
 public static void main(String []args)
{
	int x=1;
	int y=x++;
	System.out.println("x is: "+x+", y is: "+y);
	int a=1;
	int b=++a;
	System.out.println("a is: "+a+", b is: "+b);
	int i=2;
	int j=i--;
	System.out.println("i is: "+i+", j is: "+j);
	int p=2;
	int q=--p;
	System.out.println("p is: "+p+", q is: "+q);
}
}




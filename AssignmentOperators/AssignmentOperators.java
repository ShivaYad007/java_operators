class AssignmentOperators
{
	public static void main(String[] args)
{
	//a value reinitilize to every next print statement respectively
	int a=10;
        System.out.println(a+=5);//a=a+5
	System.out.println(a-=5);//a=a-5
	System.out.println(a*=5);//a=a*5
	System.out.println(a/=5);//a=a/5, which writes Quotent
	System.out.println(a%=5);//a=a%5, which writes Remainder
	System.out.println(a%=20);//a=a%5,which writes Remainder
}
}